

exports.add = function (a,b) {
    return a+b;
}

exports.sub = function(a,b) {
    return a-b;
}

exports.mul = function(a,b) {
    return a*b;
}

exports.div = function(a,b) {
    return a/b;
}

exports.mod = function(a,b) {
    return a%b;
}

exports.pow = function(a,b) {
    return Math.pow(a,b);
}